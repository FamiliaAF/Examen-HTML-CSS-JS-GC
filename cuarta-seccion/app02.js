// Ejercicio 2: Uso de estructuras condicionales
function evaluarNumero(n) {
  if (n > 0) {
    console.log("El número es positivo");
  } else if (n < 0) {
    console.log("El número es negativo");
  } else {
    console.log("El número es cero");
  }
}
evaluarNumero(10);
evaluarNumero(-5);
evaluarNumero(0);