// Ejercicio 6: Funciones y alcance de variables
function calcularAreaRectangulo(ancho, alto) {
  let area = ancho * alto;
  return area;
}

console.log("Área de 5x10:", calcularAreaRectangulo(5, 10));
console.log("Área de 7x3:", calcularAreaRectangulo(7, 3));
console.log("Área de 4x8:", calcularAreaRectangulo(4, 8));
